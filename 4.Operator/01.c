/* 산술 연산 */
#include <stdio.h>

int main() {
    int a, b;
    a = 10;
    b = 3;
    printf("a + b 는 : %d \n", a + b);  // 13
    printf("a - b 는 : %d \n", a - b);  // 7
    printf("a * b 는 : %d \n", a * b);  // 30
    printf("a / b 는 : %d \n", a / b);  // 3
    printf("a %% b 는 : %d \n", a % b); // 1
    return 0;
}